// -----------------------------
// Symbol queue
// -----------------------------
#define SYMBOL_QUEUE_SIZE    1024u
static volatile uint8_t g_symbol_queue[SYMBOL_QUEUE_SIZE];
static volatile uint16_t g_symbol_head = 0u;
static volatile uint16_t g_symbol_tail = 0u;
static volatile uint8_t g_symbol_overflow = 0u;

static inline void symbol_queue_reset(void) {
    g_symbol_head = 0u;
    g_symbol_tail = 0u;
    g_symbol_overflow = 0u;
}

static inline void symbol_queue_push_isr(uint8_t sym) {
    const uint16_t next = (uint16_t)((g_symbol_head + 1u) & (SYMBOL_QUEUE_SIZE - 1u));
    if (next == g_symbol_tail) {
        g_symbol_overflow = 1u;
        return;
    }
    g_symbol_queue[g_symbol_head] = sym;
    g_symbol_head = next;
}

static uint8_t symbol_queue_pop(uint8_t* sym) {
    if (g_symbol_tail == g_symbol_head) {
        return 0u;
    }
    *sym = g_symbol_queue[g_symbol_tail];
    g_symbol_tail = (uint16_t)((g_symbol_tail + 1u) & (SYMBOL_QUEUE_SIZE - 1u));
    return 1u;
}

// -----------------------------
// Mutable-style packet decoder
// -----------------------------
enum PacketDecoderState {
    PACKET_DECODER_STATE_SYNCING = 0,
    PACKET_DECODER_STATE_DECODING_PACKET,
    PACKET_DECODER_STATE_OK,
    PACKET_DECODER_STATE_ERROR_SYNC,
    PACKET_DECODER_STATE_ERROR_CRC,
    PACKET_DECODER_STATE_END_OF_TRANSMISSION
};

static volatile enum PacketDecoderState g_pkt_state = PACKET_DECODER_STATE_SYNCING;
static uint8_t g_expected_symbols = 0xFFu;
static uint8_t g_preamble_remaining = PREAMBLE_SYMBOLS;
static uint16_t g_sync_blank_size = 0u;
static uint8_t g_sync_armed = 0u;
static uint8_t g_symbol_count = 0u;
static uint16_t g_packet_size = 0u;
static uint16_t g_packet_count = 0u;
static uint8_t g_packet[PACKET_TOTAL_BYTES];

static void packet_decoder_init(void) {
    g_pkt_state = PACKET_DECODER_STATE_SYNCING;
    g_expected_symbols = 0xFFu;
    g_preamble_remaining = PREAMBLE_SYMBOLS;
    g_sync_blank_size = 0u;
    g_sync_armed = 0u;
    g_symbol_count = 0u;
    g_packet_size = 0u;
    g_packet_count = 0u;
}

static void packet_decoder_reset_to_sync(void) {
    g_pkt_state = PACKET_DECODER_STATE_SYNCING;
    g_expected_symbols = 0xFFu;
    g_preamble_remaining = PREAMBLE_SYMBOLS;
    g_sync_blank_size = 0u;
    g_sync_armed = 0u;
    g_symbol_count = 0u;
    g_packet_size = 0u;
}

static void packet_decoder_parse_sync(uint8_t symbol) {
    switch (symbol) {
    case SYMBOL_PAUSE:
        g_sync_blank_size++;
        if (g_sync_blank_size >= MAX_SYNC_BLANK_SYMBOLS && g_packet_count != 0u) {
            g_pkt_state = PACKET_DECODER_STATE_END_OF_TRANSMISSION;
            return;
        }
        if (g_sync_blank_size >= MIN_SYNC_PAUSES) {
            g_sync_armed = 1u;
            g_expected_symbols = (uint8_t)((1u << SYMBOL_0) | (1u << SYMBOL_1));
            g_preamble_remaining = PREAMBLE_SYMBOLS;
        }
        break;

    case SYMBOL_1:
    case SYMBOL_0:
    default:
        if (!g_sync_armed) {
            g_sync_blank_size = 0u;
            break;
        }
        if (((uint8_t)(1u << symbol) & g_expected_symbols) == 0u) {
            // Bad preamble symbol: disarm and wait for next blank run.
            g_sync_armed = 0u;
            g_sync_blank_size = 0u;
            g_expected_symbols = 0xFFu;
            g_preamble_remaining = PREAMBLE_SYMBOLS;
            break;
        }

        g_sync_blank_size = 0u;
        if (symbol == SYMBOL_1) {
            g_expected_symbols = (uint8_t)(1u << SYMBOL_0);
        } else {
            g_expected_symbols = (uint8_t)(1u << SYMBOL_1);
        }

        if (g_preamble_remaining != 0u) {
            g_preamble_remaining--;
        }
        break;
    }

    if (g_preamble_remaining == 0u) {
        g_pkt_state = PACKET_DECODER_STATE_DECODING_PACKET;
        g_packet_size = 0u;
        g_packet[g_packet_size] = 0u;
        g_symbol_count = 0u;
        BOOT_DIAG.sync_hits++;
    }
}

static enum PacketDecoderState packet_decoder_process_symbol(uint8_t symbol) {
    switch (g_pkt_state) {
    case PACKET_DECODER_STATE_SYNCING:
        packet_decoder_parse_sync(symbol);
        break;

    case PACKET_DECODER_STATE_DECODING_PACKET:
        if (symbol == SYMBOL_PAUSE) {
            g_pkt_state = PACKET_DECODER_STATE_ERROR_SYNC;
            break;
        }

        g_packet[g_packet_size] = (uint8_t)((g_packet[g_packet_size] << 1) | (symbol & 1u));
        g_symbol_count++;

        if (g_symbol_count == 8u) {
            g_symbol_count = 0u;
            g_packet_size++;

            if (g_packet_size == PACKET_TOTAL_BYTES) {
                const uint32_t crc = crc32_buf(g_packet, PACKET_SIZE_BYTES);
                const uint32_t expected_crc =
                    ((uint32_t)g_packet[PACKET_SIZE_BYTES + 0u] << 24) |
                    ((uint32_t)g_packet[PACKET_SIZE_BYTES + 1u] << 16) |
                    ((uint32_t)g_packet[PACKET_SIZE_BYTES + 2u] << 8)  |
                    ((uint32_t)g_packet[PACKET_SIZE_BYTES + 3u] << 0);

                if (crc == expected_crc) {
                    g_pkt_state = PACKET_DECODER_STATE_OK;
                    BOOT_DIAG.aux = 0u;
                } else {
                    g_pkt_state = PACKET_DECODER_STATE_ERROR_CRC;
                }
                g_packet_count++;
            } else {
                g_packet[g_packet_size] = 0u;
            }
        }
        break;

    case PACKET_DECODER_STATE_OK:
    case PACKET_DECODER_STATE_ERROR_SYNC:
    case PACKET_DECODER_STATE_ERROR_CRC:
    case PACKET_DECODER_STATE_END_OF_TRANSMISSION:
    default:
        break;
    }

    return g_pkt_state;
}

// -----------------------------
// Demod state (edge-timed)
// -----------------------------
static volatile uint8_t g_have_edge = 0u;
static volatile uint16_t g_last_edge_tick = 0u;
static volatile uint8_t g_swallow = FSK_SWALLOW_SYMBOLS;
static volatile uint8_t g_pa8_seen = 0u;

static inline uint8_t classify_symbol(uint16_t dt) {
    if (dt >= FSK_ONE_PAUSE_THRESH_US) {
        return SYMBOL_PAUSE;
    }
    if (dt >= FSK_ZERO_ONE_THRESH_US) {
        return SYMBOL_1;
    }
    return SYMBOL_0;
}

static inline void duration_hist_update(uint16_t dt) {
    BOOT_EDGE_DIAG.dt_last = dt;
    if (dt < BOOT_EDGE_DIAG.dt_min) {
        BOOT_EDGE_DIAG.dt_min = dt;
    }
    if (dt > BOOT_EDGE_DIAG.dt_max) {
        BOOT_EDGE_DIAG.dt_max = dt;
    }

    if (dt < 100u) {
        BOOT_EDGE_DIAG.dt_lt100++;
    } else if (dt < 250u) {
        BOOT_EDGE_DIAG.dt_100_250++;
    } else if (dt < 400u) {
        BOOT_EDGE_DIAG.dt_250_400++;
    } else if (dt < 650u) {
        BOOT_EDGE_DIAG.dt_400_650++;
    } else if (dt < 900u) {
        BOOT_EDGE_DIAG.dt_650_900++;
    } else if (dt < 1300u) {
        BOOT_EDGE_DIAG.dt_900_1300++;
    } else if (dt < 2200u) {
        BOOT_EDGE_DIAG.dt_1300_2200++;
    } else {
        BOOT_EDGE_DIAG.dt_ge2200++;
    }
}

void EXTI4_15_IRQHandler(void) {
    const uint32_t bit = (1u << PIN_PA8);
    if ((EXTI_PR & bit) == 0u) {
        return;
    }
    EXTI_PR = bit;

    const uint8_t level = read_pa8();
    g_pa8_seen = 1u;
    BOOT_DIAG.edges++;
    if (level) {
        BOOT_EDGE_DIAG.rising_edges++;
    } else {
        BOOT_EDGE_DIAG.falling_edges++;
    }

    const uint16_t now = (uint16_t)TIM14_CNT;
    if (!g_have_edge) {
        g_have_edge = 1u;
        g_last_edge_tick = now;
        return;
    }

    const uint16_t dt = (uint16_t)(now - g_last_edge_tick);
    g_last_edge_tick = now;

    if (dt < FSK_GLITCH_US) {
        return;
    }

    duration_hist_update(dt);

    uint8_t symbol;
    if (dt >= FSK_TIMEOUT_US) {
        symbol = SYMBOL_PAUSE;
        g_swallow = FSK_SWALLOW_SYMBOLS;
        BOOT_DIAG.timing_reset++;
    } else {
        symbol = classify_symbol(dt);
    }

    if (g_swallow != 0u) {
        symbol = SYMBOL_PAUSE;
        g_swallow--;
    }

    symbol_queue_push_isr(symbol);
    BOOT_DIAG.symbols++;
    BOOT_DIAG.aux = symbol;
}

// -----------------------------
// Update session state
// -----------------------------
static uint8_t g_update_started = 0u;
static uint8_t g_update_done = 0u;
static uint8_t g_update_error = 0u;
static uint32_t g_update_done_ms = 0u;

static uint32_t g_image_size = 0u;
static uint32_t g_image_crc32 = 0u;
static uint32_t g_bytes_written = 0u;
static uint32_t g_write_addr = APP_BASE_ADDR;
static uint8_t g_pending_valid = 0u;
static uint8_t g_pending_byte = 0u;

static uint32_t g_last_activity_ms = 0u;

static uint32_t load_le32(const uint8_t* p) {
    return ((uint32_t)p[0]) |
           ((uint32_t)p[1] << 8) |
           ((uint32_t)p[2] << 16) |
           ((uint32_t)p[3] << 24);
}

static void abort_update(uint32_t error_code) {
    flash_lock();
    g_update_started = 0u;
    g_update_error = 1u;
    BOOT_DIAG.last_error_code = error_code;
}

static uint8_t begin_update_from_header(const uint8_t* pkt) {
    const uint32_t magic = load_le32(&pkt[0]);
    const uint32_t size = load_le32(&pkt[4]);
    const uint32_t crc = load_le32(&pkt[8]);

    if (magic != HEADER_MAGIC || size == 0u || size > APP_MAX_BYTES) {
        return 0u;
    }

    if (!flash_unlock()) {
        return 0u;
    }

    const uint32_t erase_bytes = (size + FLASH_PAGE_BYTES - 1u) & ~(FLASH_PAGE_BYTES - 1u);
    const uint32_t erase_end = APP_BASE_ADDR + erase_bytes;
    for (uint32_t addr = APP_BASE_ADDR; addr < erase_end; addr += FLASH_PAGE_BYTES) {
        if (!flash_erase_page(addr)) {
            flash_lock();
            return 0u;
        }
    }

    g_image_size = size;
    g_image_crc32 = crc;
    g_bytes_written = 0u;
    g_write_addr = APP_BASE_ADDR;
    g_pending_valid = 0u;
    g_pending_byte = 0u;

    g_update_started = 1u;
    g_update_done = 0u;
    g_update_error = 0u;
    g_update_done_ms = 0u;

    BOOT_DIAG.image_size = size;
    BOOT_DIAG.image_crc32 = crc;
    BOOT_DIAG.bytes_written = 0u;
    BOOT_DIAG.last_error_code = 0u;

    return 1u;
}

static uint8_t flush_pending_halfword(void) {
    if (!g_pending_valid) {
        return 1u;
    }

    const uint16_t hw = (uint16_t)g_pending_byte | 0xFF00u;
    if (!flash_program_halfword(g_write_addr, hw)) {
        return 0u;
    }

    g_write_addr += 2u;
    g_pending_valid = 0u;
    return 1u;
}

static uint8_t write_stream_bytes(const uint8_t* data, uint16_t len) {
    for (uint16_t i = 0u; i < len; ++i) {
        if (g_bytes_written >= g_image_size) {
            break;
        }

        const uint8_t b = data[i];
        if (!g_pending_valid) {
            g_pending_byte = b;
            g_pending_valid = 1u;
        } else {
            const uint16_t hw = (uint16_t)g_pending_byte | ((uint16_t)b << 8);
            if (!flash_program_halfword(g_write_addr, hw)) {
                return 0u;
            }
            g_write_addr += 2u;
            g_pending_valid = 0u;
        }

        g_bytes_written++;
    }

    BOOT_DIAG.bytes_written = g_bytes_written;
    return 1u;
}

static uint8_t finalize_update_if_complete(void) {
    if (!g_update_started || g_bytes_written < g_image_size) {
        return 1u;
    }

    if (!flush_pending_halfword()) {
        return 0u;
    }

    flash_lock();

    const uint32_t crc = crc32_flash(APP_BASE_ADDR, g_image_size);
    if (crc != g_image_crc32) {
        return 0u;
    }

    uint32_t* app = (uint32_t*)APP_BASE_ADDR;
    if (!is_valid_app(app[0], app[1])) {
        return 0u;
    }

    g_update_done = 1u;
    g_update_done_ms = now_ms();
    g_update_started = 0u;
    return 1u;
}

static uint8_t process_data_packet(const uint8_t* pkt) {
    if (!g_update_started) {
        return 0u;
    }

    if (!write_stream_bytes(pkt, PACKET_SIZE_BYTES)) {
        return 0u;
    }

    if (!finalize_update_if_complete()) {
        return 0u;
    }

    return 1u;
}

static uint8_t handle_packet(const uint8_t* pkt) {
    if (load_le32(&pkt[0]) == HEADER_MAGIC) {
        if (!begin_update_from_header(pkt)) {
            BOOT_DIAG.start_fail++;
            BOOT_DIAG.last_error_code = 2u;
            return 0u;
        }
        return 1u;
    }

    if (!g_update_started) {
        return 1u;
    }

    if (!process_data_packet(pkt)) {
        BOOT_DIAG.data_fail++;
        BOOT_DIAG.last_error_code = 3u;
        return 0u;
    }

    return 1u;
}

int main(void) {
    hw_init();
    time_init();
    symbol_queue_reset();
    packet_decoder_init();

    BOOT_DIAG.magic = 0x49314244u; // "I1BD"
    BOOT_DIAG.edges = 0u;
    BOOT_DIAG.symbols = 0u;
    BOOT_DIAG.sync_hits = 0u;
    BOOT_DIAG.frames_ok = 0u;
    BOOT_DIAG.crc_fail = 0u;
    BOOT_DIAG.start_fail = 0u;
    BOOT_DIAG.data_fail = 0u;
    BOOT_DIAG.end_fail = 0u;
    BOOT_DIAG.timing_reset = 0u;
    BOOT_DIAG.idle_abort = 0u;
    BOOT_DIAG.bytes_written = 0u;
    BOOT_DIAG.image_size = 0u;
    BOOT_DIAG.image_crc32 = 0u;
    BOOT_DIAG.last_error_code = 0u;
    BOOT_DIAG.aux = 0u;

    BOOT_EDGE_DIAG.magic = 0x45314447u; // "E1DG"
    BOOT_EDGE_DIAG.rising_edges = 0u;
    BOOT_EDGE_DIAG.falling_edges = 0u;
    BOOT_EDGE_DIAG.dt_lt100 = 0u;
    BOOT_EDGE_DIAG.dt_100_250 = 0u;
    BOOT_EDGE_DIAG.dt_250_400 = 0u;
    BOOT_EDGE_DIAG.dt_400_650 = 0u;
    BOOT_EDGE_DIAG.dt_650_900 = 0u;
    BOOT_EDGE_DIAG.dt_900_1300 = 0u;
    BOOT_EDGE_DIAG.dt_1300_2200 = 0u;
    BOOT_EDGE_DIAG.dt_ge2200 = 0u;
    BOOT_EDGE_DIAG.dt_min = 0xFFFFFFFFu;
    BOOT_EDGE_DIAG.dt_max = 0u;
    BOOT_EDGE_DIAG.dt_last = 0u;
    BOOT_EDGE_DIAG.stream_select = 0u;
    BOOT_EDGE_DIAG.rise_score = 0u;
    BOOT_EDGE_DIAG.fall_score = 0u;

    g_have_edge = 0u;
    g_last_edge_tick = 0u;
    g_swallow = FSK_SWALLOW_SYMBOLS;
    g_pa8_seen = 0u;

    const uint32_t* app = (const uint32_t*)APP_BASE_ADDR;
    const uint8_t app_valid = is_valid_app(app[0], app[1]);
    const uint8_t force_boot_button = sample_force_boot_latched();
    const uint8_t force_boot = (BOOT_TEST_ALWAYS != 0u) ? 1u : force_boot_button;

    // Production behavior: if button is not held, jump straight to main app.
    // This prevents any bootloader blink pattern from appearing during normal power-up.
    if (app_valid && !force_boot) {
        jump_to_app();
    }

    g_last_activity_ms = now_ms();
    uint32_t last_led_toggle_ms = g_last_activity_ms;
    uint8_t led_state = 0u;

    enable_irq();

    while (1) {
        const uint32_t ms = now_ms();

        uint8_t symbol = 0u;
        while (symbol_queue_pop(&symbol)) {
            g_last_activity_ms = ms;

            const enum PacketDecoderState s = packet_decoder_process_symbol(symbol);
            if (s == PACKET_DECODER_STATE_OK) {
                if (handle_packet(g_packet)) {
                    BOOT_DIAG.frames_ok++;
                    BOOT_DIAG.last_error_code = 0u;
                } else {
                    abort_update(8u);
                }
                packet_decoder_reset_to_sync();
            } else if (s == PACKET_DECODER_STATE_ERROR_CRC) {
                BOOT_DIAG.crc_fail++;
                packet_decoder_reset_to_sync();
                if (g_update_started) {
                    abort_update(4u);
                }
            } else if (s == PACKET_DECODER_STATE_ERROR_SYNC) {
                packet_decoder_reset_to_sync();
                if (g_update_started) {
                    abort_update(5u);
                }
            } else if (s == PACKET_DECODER_STATE_END_OF_TRANSMISSION) {
                packet_decoder_reset_to_sync();
                BOOT_DIAG.end_fail++;
                if (g_update_started) {
                    abort_update(6u);
                }
            }
        }

        if (g_symbol_overflow) {
            g_symbol_overflow = 0u;
            BOOT_DIAG.last_error_code = 9u;
            if (g_update_started) {
                abort_update(9u);
            }
            packet_decoder_reset_to_sync();
            symbol_queue_reset();
        }

        if (g_update_done) {
            status_led_set(1u);
            step_leds_all(1u);
            if ((ms - g_update_done_ms) >= UPDATE_DONE_HOLD_MS) {
                jump_to_app();
                g_update_done = 0u;
            }
            continue;
        }

        if (g_update_started && (ms - g_last_activity_ms) > RX_IDLE_ABORT_MS) {
            BOOT_DIAG.idle_abort++;
            abort_update(7u);
            packet_decoder_reset_to_sync();
            symbol_queue_reset();
        }

        status_led_set(1u);
        if (g_update_started) {
            step_leds_progress(g_bytes_written, g_image_size);
        } else if (g_update_error) {
            if ((ms - last_led_toggle_ms) >= 120u) {
                last_led_toggle_ms = ms;
                led_state ^= 1u;
                step_leds_all(led_state);
            }
        } else {
            // Bootloader idle indicator (only visible when boot mode is forced).
            if ((ms - last_led_toggle_ms) >= 250u) {
                last_led_toggle_ms = ms;
                led_state ^= 1u;
            }
            step_leds_all(0u);
            step_led_set(0u, led_state);
            step_led_set(1u, g_pa8_seen);
        }
    }
}
