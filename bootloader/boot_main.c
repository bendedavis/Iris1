#include <stdint.h>
#include <stddef.h>

// -----------------------------
// Memory map
// -----------------------------
#define FLASH_BASE_ADDR      0x08000000u
#define FLASH_END_ADDR       0x08010000u
#define APP_BASE_ADDR        0x08002000u
#define APP_MAX_BYTES        (FLASH_END_ADDR - APP_BASE_ADDR)
#define FLASH_PAGE_BYTES     1024u

// -----------------------------
// Core/system registers
// -----------------------------
#define RCC_BASE             0x40021000u
#define RCC_AHBENR           (*(volatile uint32_t*)(RCC_BASE + 0x14u))
#define RCC_APB2ENR          (*(volatile uint32_t*)(RCC_BASE + 0x18u))
#define RCC_APB1ENR          (*(volatile uint32_t*)(RCC_BASE + 0x1Cu))

#define RCC_AHBENR_GPIOAEN   (1u << 17)
#define RCC_AHBENR_GPIOBEN   (1u << 18)
#define RCC_APB2ENR_SYSCFGEN (1u << 0)
#define RCC_APB1ENR_TIM14EN  (1u << 8)

#define SYSCFG_BASE          0x40010000u
#define SYSCFG_CFGR1         (*(volatile uint32_t*)(SYSCFG_BASE + 0x00u))
#define SYSCFG_EXTICR3       (*(volatile uint32_t*)(SYSCFG_BASE + 0x10u))

#define EXTI_BASE            0x40010400u
#define EXTI_IMR             (*(volatile uint32_t*)(EXTI_BASE + 0x00u))
#define EXTI_RTSR            (*(volatile uint32_t*)(EXTI_BASE + 0x08u))
#define EXTI_FTSR            (*(volatile uint32_t*)(EXTI_BASE + 0x0Cu))
#define EXTI_PR              (*(volatile uint32_t*)(EXTI_BASE + 0x14u))

#define NVIC_ISER            (*(volatile uint32_t*)0xE000E100u)

#define GPIOA_BASE           0x48000000u
#define GPIOA_MODER          (*(volatile uint32_t*)(GPIOA_BASE + 0x00u))
#define GPIOA_PUPDR          (*(volatile uint32_t*)(GPIOA_BASE + 0x0Cu))
#define GPIOA_IDR            (*(volatile uint32_t*)(GPIOA_BASE + 0x10u))
#define GPIOA_BSRR           (*(volatile uint32_t*)(GPIOA_BASE + 0x18u))

#define GPIOB_BASE           0x48000400u
#define GPIOB_MODER          (*(volatile uint32_t*)(GPIOB_BASE + 0x00u))
#define GPIOB_BSRR           (*(volatile uint32_t*)(GPIOB_BASE + 0x18u))

#define TIM14_BASE           0x40002000u
#define TIM14_CR1            (*(volatile uint32_t*)(TIM14_BASE + 0x00u))
#define TIM14_CNT            (*(volatile uint32_t*)(TIM14_BASE + 0x24u))
#define TIM14_PSC            (*(volatile uint32_t*)(TIM14_BASE + 0x28u))
#define TIM14_ARR            (*(volatile uint32_t*)(TIM14_BASE + 0x2Cu))

#define FLASH_IF_BASE        0x40022000u
#define FLASH_KEYR           (*(volatile uint32_t*)(FLASH_IF_BASE + 0x04u))
#define FLASH_SR             (*(volatile uint32_t*)(FLASH_IF_BASE + 0x0Cu))
#define FLASH_CR             (*(volatile uint32_t*)(FLASH_IF_BASE + 0x10u))
#define FLASH_AR             (*(volatile uint32_t*)(FLASH_IF_BASE + 0x14u))

#define FLASH_KEY1           0x45670123u
#define FLASH_KEY2           0xCDEF89ABu

#define FLASH_SR_BSY         (1u << 0)
#define FLASH_SR_PGERR       (1u << 2)
#define FLASH_SR_WRPRTERR    (1u << 4)
#define FLASH_SR_EOP         (1u << 5)

#define FLASH_CR_PG          (1u << 0)
#define FLASH_CR_PER         (1u << 1)
#define FLASH_CR_STRT        (1u << 6)
#define FLASH_CR_LOCK        (1u << 7)

// -----------------------------
// Pins
// -----------------------------
#define PIN_PA3              3u   // mute button input (active-low)
#define PIN_PA8              8u   // audio/clock input
#define PIN_PA12             12u  // mute LED

static const uint8_t kStepLedPins[8] = { 9u, 10u, 11u, 14u, 13u, 8u, 12u, 15u };

// -----------------------------
// Mutable-style FSK symbols
// -----------------------------
#define SYMBOL_0                0u
#define SYMBOL_1                1u
#define SYMBOL_PAUSE            2u

// Symbol periods generated by WAV tool:
// 0 -> 64 samples (~1333 us), 1 -> 128 samples (~2667 us), pause -> 512 samples (~10667 us)
#define FSK_ZERO_ONE_THRESH_US  2000u
#define FSK_ONE_PAUSE_THRESH_US 6667u
#define FSK_GLITCH_US            300u
#define FSK_TIMEOUT_US         18000u
#define FSK_SWALLOW_SYMBOLS      4u

#define PACKET_SIZE_BYTES        256u
#define PACKET_TOTAL_BYTES       (PACKET_SIZE_BYTES + 4u)
#define PREAMBLE_SYMBOLS         32u
#define MIN_SYNC_PAUSES          2u
#define MAX_SYNC_BLANK_SYMBOLS   500u

#define HEADER_MAGIC             0x31495249u  // "IRI1" little-endian in packet[0..3]

// Boot behavior
#define RX_IDLE_ABORT_MS     3000u
#define UPDATE_DONE_HOLD_MS  1000u
#define BOOT_TEST_ALWAYS     0u

#define SRAM_BASE            0x20000000u
#define SRAM_END             0x20002000u

typedef void (*entry_fn_t)(void);

static inline void set_msp(uint32_t top_of_main_stack) {
    __asm volatile ("msr msp, %0" : : "r" (top_of_main_stack) : );
}

static inline void disable_irq(void) {
    __asm volatile ("cpsid i" ::: "memory");
}

static inline void enable_irq(void) {
    __asm volatile ("cpsie i" ::: "memory");
}

static uint8_t is_valid_app(uint32_t sp, uint32_t reset) {
    if (sp < SRAM_BASE || sp > SRAM_END) return 0u;
    if (reset < APP_BASE_ADDR || reset >= FLASH_END_ADDR) return 0u;
    if (sp == 0xFFFFFFFFu || reset == 0xFFFFFFFFu) return 0u;
    return 1u;
}

static void remap_vector_table_to_sram(const uint32_t* app_vectors) {
    volatile uint32_t* sram_vectors = (uint32_t*)SRAM_BASE;
    for (uint32_t i = 0u; i < 48u; ++i) {
        sram_vectors[i] = app_vectors[i];
    }

    RCC_APB2ENR |= RCC_APB2ENR_SYSCFGEN;
    SYSCFG_CFGR1 = (SYSCFG_CFGR1 & ~0x3u) | 0x3u; // MEM_MODE = SRAM @ 0x00000000
}

static void jump_to_app(void) {
    uint32_t* app = (uint32_t*)APP_BASE_ADDR;
    const uint32_t app_sp = app[0];
    const uint32_t app_reset = app[1];

    if (!is_valid_app(app_sp, app_reset)) {
        return;
    }

    disable_irq();
    remap_vector_table_to_sram(app);
    set_msp(app_sp);
    ((entry_fn_t)app_reset)();
}

// -----------------------------
// Minimal hardware init
// -----------------------------
static void hw_init(void) {
    RCC_AHBENR |= RCC_AHBENR_GPIOAEN | RCC_AHBENR_GPIOBEN;
    RCC_APB1ENR |= RCC_APB1ENR_TIM14EN;
    RCC_APB2ENR |= RCC_APB2ENR_SYSCFGEN;

    // PA8 input with pull-up.
    GPIOA_MODER &= ~(0x3u << (PIN_PA8 * 2u));
    GPIOA_PUPDR &= ~(0x3u << (PIN_PA8 * 2u));
    GPIOA_PUPDR |=  (0x1u << (PIN_PA8 * 2u));

    // PA3 input with pull-up.
    GPIOA_MODER &= ~(0x3u << (PIN_PA3 * 2u));
    GPIOA_PUPDR &= ~(0x3u << (PIN_PA3 * 2u));
    GPIOA_PUPDR |=  (0x1u << (PIN_PA3 * 2u));

    // PA12 output push-pull.
    GPIOA_MODER &= ~(0x3u << (PIN_PA12 * 2u));
    GPIOA_MODER |=  (0x1u << (PIN_PA12 * 2u));
    GPIOA_BSRR = (1u << (PIN_PA12 + 16u));

    // PB step LEDs (logical 1..8) outputs.
    for (uint8_t i = 0u; i < 8u; ++i) {
        const uint8_t pin = kStepLedPins[i];
        GPIOB_MODER &= ~(0x3u << (pin * 2u));
        GPIOB_MODER |=  (0x1u << (pin * 2u));
        GPIOB_BSRR = (1u << (pin + 16u));
    }

    // TIM14 free-running at 1 MHz (HSI 8 MHz / (PSC+1=8)).
    TIM14_PSC = 7u;
    TIM14_ARR = 0xFFFFu;
    TIM14_CNT = 0u;
    TIM14_CR1 = 1u;

    // EXTI line8 from PA8, both edges.
    SYSCFG_EXTICR3 &= ~(0xFu << 0u); // EXTI8 -> PA8
    EXTI_IMR  |= (1u << PIN_PA8);
    EXTI_RTSR |= (1u << PIN_PA8);
    EXTI_FTSR |= (1u << PIN_PA8);
    EXTI_PR = (1u << PIN_PA8);

    // EXTI4_15 IRQ enable (IRQn=7).
    NVIC_ISER = (1u << 7);
}

static inline uint8_t read_pa8(void) {
    return (uint8_t)((GPIOA_IDR >> PIN_PA8) & 0x1u);
}

static inline uint8_t read_pa3_pressed(void) {
    return (uint8_t)(((GPIOA_IDR >> PIN_PA3) & 0x1u) == 0u);
}

static inline void status_led_set(uint8_t on) {
    if (on) {
        GPIOA_BSRR = (1u << PIN_PA12);
    } else {
        GPIOA_BSRR = (1u << (PIN_PA12 + 16u));
    }
}

static inline void step_led_set(uint8_t index, uint8_t on) {
    if (index >= 8u) return;
    const uint8_t pin = kStepLedPins[index];
    if (on) {
        GPIOB_BSRR = (1u << pin);
    } else {
        GPIOB_BSRR = (1u << (pin + 16u));
    }
}

static void step_leds_all(uint8_t on) {
    for (uint8_t i = 0u; i < 8u; ++i) {
        step_led_set(i, on);
    }
}

static void step_leds_progress(uint32_t done, uint32_t total) {
    uint8_t lit = 0u;
    if (total != 0u) {
        lit = (uint8_t)((done * 8u) / total);
        if (done >= total) {
            lit = 8u;
        }
    }
    for (uint8_t i = 0u; i < 8u; ++i) {
        step_led_set(i, (uint8_t)(i < lit));
    }
}

// -----------------------------
// Timebase from TIM14
// -----------------------------
static uint16_t g_last_tim14 = 0u;
static uint32_t g_time_us = 0u;

static void time_init(void) {
    g_last_tim14 = (uint16_t)TIM14_CNT;
    g_time_us = 0u;
}

static void time_update(void) {
    const uint16_t now = (uint16_t)TIM14_CNT;
    const uint16_t delta = (uint16_t)(now - g_last_tim14);
    g_last_tim14 = now;
    g_time_us += (uint32_t)delta;
}

static uint32_t now_ms(void) {
    time_update();
    return g_time_us / 1000u;
}

static uint8_t sample_force_boot_latched(void) {
    uint32_t pressed_samples = 0u;
    uint32_t total_samples = 0u;
    const uint32_t start_ms = now_ms();
    while ((now_ms() - start_ms) < 40u) {
        if (read_pa3_pressed()) {
            pressed_samples++;
        }
        total_samples++;
    }
    if (total_samples == 0u) {
        return 0u;
    }
    return (pressed_samples * 4u >= total_samples * 3u) ? 1u : 0u;
}

// -----------------------------
// CRC helpers
// -----------------------------
static uint32_t crc32_flash(uint32_t addr, uint32_t size) {
    uint32_t crc = 0xFFFFFFFFu;
    for (uint32_t i = 0u; i < size; ++i) {
        const uint8_t b = *(volatile const uint8_t*)(addr + i);
        crc ^= (uint32_t)b;
        for (uint8_t j = 0u; j < 8u; ++j) {
            if (crc & 1u) {
                crc = (crc >> 1) ^ 0xEDB88320u;
            } else {
                crc >>= 1;
            }
        }
    }
    return crc ^ 0xFFFFFFFFu;
}

static uint32_t crc32_buf(const uint8_t* data, uint32_t size) {
    uint32_t crc = 0xFFFFFFFFu;
    for (uint32_t i = 0u; i < size; ++i) {
        crc ^= (uint32_t)data[i];
        for (uint8_t j = 0u; j < 8u; ++j) {
            if (crc & 1u) {
                crc = (crc >> 1) ^ 0xEDB88320u;
            } else {
                crc >>= 1;
            }
        }
    }
    return crc ^ 0xFFFFFFFFu;
}

// -----------------------------
// Flash programming helpers
// -----------------------------
static void flash_wait_ready(void) {
    while (FLASH_SR & FLASH_SR_BSY) {
        // wait
    }
}

static uint8_t flash_unlock(void) {
    if (FLASH_CR & FLASH_CR_LOCK) {
        FLASH_KEYR = FLASH_KEY1;
        FLASH_KEYR = FLASH_KEY2;
    }
    return (uint8_t)((FLASH_CR & FLASH_CR_LOCK) == 0u);
}

static void flash_lock(void) {
    FLASH_CR |= FLASH_CR_LOCK;
}

static void flash_clear_status(void) {
    FLASH_SR = FLASH_SR_EOP | FLASH_SR_PGERR | FLASH_SR_WRPRTERR;
}

static uint8_t flash_erase_page(uint32_t addr) {
    flash_wait_ready();
    flash_clear_status();

    FLASH_CR |= FLASH_CR_PER;
    FLASH_AR = addr;
    FLASH_CR |= FLASH_CR_STRT;

    flash_wait_ready();
    FLASH_CR &= ~FLASH_CR_PER;

    if (FLASH_SR & (FLASH_SR_PGERR | FLASH_SR_WRPRTERR)) {
        return 0u;
    }
    flash_clear_status();
    return 1u;
}

static uint8_t flash_program_halfword(uint32_t addr, uint16_t data) {
    flash_wait_ready();
    flash_clear_status();

    FLASH_CR |= FLASH_CR_PG;
    *(volatile uint16_t*)addr = data;
    flash_wait_ready();
    FLASH_CR &= ~FLASH_CR_PG;

    if (FLASH_SR & (FLASH_SR_PGERR | FLASH_SR_WRPRTERR)) {
        return 0u;
    }
    if (*(volatile uint16_t*)addr != data) {
        return 0u;
    }
    flash_clear_status();
    return 1u;
}

